package aqa.course.tasks.step2;

public class Task2 {
    public static void main(String[] args) {
        int variable = 1;
        System.out.println("Variable: " + variable);
        System.out.println("Variable after post increment: " + variable++);
        System.out.println("Variable after pre increment: " + ++variable);
        System.out.println("Variable after post decrement: " + variable--);
        System.out.println("Variable after pre decrement: " + --variable);
    }
}
